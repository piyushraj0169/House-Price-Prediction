"""PDF report generator helper using ReportLab."""

from __future__ import annotations

import io
from datetime import datetime
from reportlab.lib import colors
from reportlab.lib.pagesizes import letter
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import inch

from src.utils import format_indian_currency


def generate_prediction_pdf(
    area: float,
    bedrooms: int,
    bathrooms: int,
    age: int,
    location: str,
    predicted_price: float,
    lower_bound: float,
    upper_bound: float,
    model_name: str,
    r2_score: float,
    mae: float,
    rmse: float,
) -> bytes:
    """Generate a stylized PDF report of the property price prediction and return bytes."""
    buffer = io.BytesIO()
    
    # Page setup
    doc = SimpleDocTemplate(
        buffer,
        pagesize=letter,
        rightMargin=54,
        leftMargin=54,
        topMargin=54,
        bottomMargin=54
    )
    
    styles = getSampleStyleSheet()
    
    # Custom colors matching UI palette
    primary_color = colors.HexColor("#0f172a")    # Slate-900 (Header background)
    secondary_color = colors.HexColor("#1e293b")  # Slate-800
    accent_green = colors.HexColor("#22c55e")     # Tailwind Green
    accent_blue = colors.HexColor("#38bdf8")      # Tailwind Sky
    text_color = colors.HexColor("#334155")       # Slate-600
    light_bg = colors.HexColor("#f8fafc")         # Slate-50
    border_color = colors.HexColor("#cbd5e1")     # Slate-300
    
    # Custom styles
    title_style = ParagraphStyle(
        name="DocTitle",
        parent=styles["Normal"],
        fontName="Helvetica-Bold",
        fontSize=24,
        textColor=colors.white,
        alignment=1, # Center
        spaceAfter=6
    )
    
    subtitle_style = ParagraphStyle(
        name="DocSubTitle",
        parent=styles["Normal"],
        fontName="Helvetica-Oblique",
        fontSize=12,
        textColor=colors.HexColor("#94a3b8"),
        alignment=1, # Center
        spaceAfter=15
    )
    
    h1_style = ParagraphStyle(
        name="Heading1_Custom",
        parent=styles["Normal"],
        fontName="Helvetica-Bold",
        fontSize=16,
        textColor=primary_color,
        spaceBefore=14,
        spaceAfter=8,
        borderPadding=(0, 0, 2, 0),
        borderColor=border_color,
        borderWidth=0.5
    )
    
    body_style = ParagraphStyle(
        name="Body_Custom",
        parent=styles["Normal"],
        fontName="Helvetica",
        fontSize=10,
        textColor=text_color,
        leading=14,
        spaceAfter=8
    )
    
    bold_body_style = ParagraphStyle(
        name="BoldBody_Custom",
        parent=styles["Normal"],
        fontName="Helvetica-Bold",
        fontSize=10,
        textColor=primary_color,
        spaceAfter=8
    )
    
    disclaimer_style = ParagraphStyle(
        name="Disclaimer_Custom",
        parent=styles["Normal"],
        fontName="Helvetica-Oblique",
        fontSize=8,
        textColor=colors.HexColor("#64748b"),
        leading=11,
        spaceBefore=15
    )
    
    story = []
    
    # 1. Header Banner / Project Branding
    banner_data = [
        [Paragraph("PROPERTY VALUATION REPORT", title_style)],
        [Paragraph("Generated by House Price Prediction AI Service", subtitle_style)]
    ]
    banner_table = Table(banner_data, colWidths=[doc.width])
    banner_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), primary_color),
        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('TOPPADDING', (0, 0), (-1, -1), 20),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 10),
    ]))
    story.append(banner_table)
    story.append(Spacer(1, 15))
    
    # Report Metadata
    report_date = datetime.now().strftime("%B %d, %Y - %I:%M %p")
    meta_style = ParagraphStyle(
        name="Metadata",
        parent=styles["Normal"],
        fontName="Helvetica",
        fontSize=9,
        textColor=colors.HexColor("#64748b"),
        alignment=2 # Right
    )
    story.append(Paragraph(f"<b>Prediction Date:</b> {report_date}", meta_style))
    story.append(Spacer(1, 10))
    
    # 2. Valuation Summary Section
    story.append(Paragraph("Estimated Market Valuation", h1_style))
    
    formatted_pred = format_indian_currency(predicted_price, decimals=0)
    formatted_lower = format_indian_currency(lower_bound, decimals=0)
    formatted_upper = format_indian_currency(upper_bound, decimals=0)
    
    pred_card_style = ParagraphStyle(
        name="PredCard",
        parent=styles["Normal"],
        fontName="Helvetica-Bold",
        fontSize=20,
        textColor=accent_green,
        alignment=1
    )
    
    range_card_style = ParagraphStyle(
        name="RangeCard",
        parent=styles["Normal"],
        fontName="Helvetica-Bold",
        fontSize=12,
        textColor=accent_blue,
        alignment=1
    )
    
    val_data = [
        [Paragraph("ESTIMATED PRICE", ParagraphStyle(name="lbl1", parent=body_style, alignment=1, fontSize=9, fontName="Helvetica-Bold", textColor=colors.HexColor("#94a3b8")))],
        [Paragraph(formatted_pred, pred_card_style)],
        [Spacer(1, 4)],
        [Paragraph(f"Expected Range (95% Confidence Interval)", ParagraphStyle(name="lbl2", parent=body_style, alignment=1, fontSize=9, fontName="Helvetica-Bold", textColor=colors.HexColor("#64748b")))],
        [Paragraph(f"{formatted_lower} - {formatted_upper}", range_card_style)]
    ]
    val_table = Table(val_data, colWidths=[doc.width])
    val_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), light_bg),
        ('BOX', (0, 0), (-1, -1), 1, border_color),
        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('TOPPADDING', (0, 0), (-1, -1), 15),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 15),
        ('LINELEFT', (0, 0), (0, -1), 4, accent_green), # Green accent stripe on left
    ]))
    story.append(val_table)
    story.append(Spacer(1, 15))
    
    # 3. Property Specifications
    story.append(Paragraph("Property Specifications", h1_style))
    spec_data = [
        [Paragraph("<b>Parameter</b>", bold_body_style), Paragraph("<b>Property Details</b>", bold_body_style)],
        [Paragraph("Location", body_style), Paragraph(location, body_style)],
        [Paragraph("Area", body_style), Paragraph(f"{area:,.0f} sq ft", body_style)],
        [Paragraph("Bedrooms", body_style), Paragraph(str(int(bedrooms)), body_style)],
        [Paragraph("Bathrooms", body_style), Paragraph(str(int(bathrooms)), body_style)],
        [Paragraph("Property Age", body_style), Paragraph(f"{int(age)} years", body_style)],
    ]
    spec_table = Table(spec_data, colWidths=[doc.width * 0.4, doc.width * 0.6])
    spec_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor("#e2e8f0")),
        ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, light_bg]),
        ('GRID', (0, 0), (-1, -1), 0.5, border_color),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]))
    story.append(spec_table)
    story.append(Spacer(1, 15))
    
    # 4. Model Analytics & Calibration
    story.append(Paragraph("Model Calibration & Analytics", h1_style))
    analytics_data = [
        [Paragraph("<b>Metric</b>", bold_body_style), Paragraph("<b>Value</b>", bold_body_style), Paragraph("<b>Description</b>", bold_body_style)],
        [Paragraph("Model Algorithm", body_style), Paragraph(model_name, body_style), Paragraph("Predictive model algorithm selected by evaluation performance.", body_style)],
        [Paragraph("Model R² Score", body_style), Paragraph(f"{r2_score:.4f}", body_style), Paragraph("Proportion of price variance explained by property specifications.", body_style)],
        [Paragraph("Mean Absolute Error (MAE)", body_style), Paragraph(format_indian_currency(mae, decimals=2), body_style), Paragraph("Average expected difference from transaction price.", body_style)],
        [Paragraph("Root Mean Squared Error (RMSE)", body_style), Paragraph(format_indian_currency(rmse, decimals=2), body_style), Paragraph("Typical deviation range (quadratic scale for large errors).", body_style)],
    ]
    analytics_table = Table(analytics_data, colWidths=[doc.width * 0.3, doc.width * 0.2, doc.width * 0.5])
    analytics_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor("#e2e8f0")),
        ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, light_bg]),
        ('GRID', (0, 0), (-1, -1), 0.5, border_color),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]))
    story.append(analytics_table)
    story.append(Spacer(1, 15))
    
    # 5. Explanatory & Disclaimer Section
    story.append(Paragraph("<b>Explanatory Note</b>", ParagraphStyle(name="Expllbl", parent=bold_body_style, fontSize=10, textColor=secondary_color)))
    explanation_text = (
        "The estimated valuation and range are generated dynamically by artificial intelligence and machine learning regression algorithms. "
        "The model is trained against local historical housing transactions, utilizing features such as total built-up area, counts of rooms/bathrooms, and property age. "
        "The 95% confidence interval is calibrated using holdout evaluation residuals (errors observed in testing) to represent standard variations in final selling prices."
    )
    story.append(Paragraph(explanation_text, body_style))
    
    disclaimer_text = (
        "<b>Disclaimer:</b> This report is for informational purposes only and does not constitute a formal appraisal or financial advice. "
        "Real estate transaction prices fluctuate based on external parameters not captured in standard models, including building condition, interior finishes, "
        "precise local amenities, street access, and macroeconomic developments. The service developers hold no liability for transaction choices based on this report."
    )
    story.append(Paragraph(disclaimer_text, disclaimer_style))
    
    # Build Document
    doc.build(story)
    
    # Get bytes
    pdf_bytes = buffer.getvalue()
    buffer.close()
    
    return pdf_bytes
